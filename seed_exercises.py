def seed():
    db = SessionLocal()
    existing_count = db.query(models.ExerciseLibrary).count()
    if existing_count > 0:
        print(f"В базе уже {existing_count} упражнений. Пропускаем seed.")
        db.close()
        return

    for name, muscle_group, equipment in EXERCISES:
        db.add(models.ExerciseLibrary(
            name=name,
            muscle_group=muscle_group,
            equipment=equipment,
        ))
    db.commit()
    db.close()
    print(f"Добавлено {len(EXERCISES)} упражнений в exercise_library.")


if __name__ == "__main__":
    seed()